int	ft_sqrt(int nb)
{
	int	start;
	int	sqrt;

	start = 1;
	sqrt = 0;
	while (nb > 0)
	{
		nb -= start;
		start += 2;
		sqrt++;
	}
	if (nb < 0)
		return (0);
	return (sqrt);
}
