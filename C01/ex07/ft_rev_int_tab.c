void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	temp;

	i = 0;
	while (i < size / 2)
	{
		j = size - (i + 1);
		temp = tab[j];
		tab[j] = tab[i];
		tab[i] = temp;
		i++;
	}
}
